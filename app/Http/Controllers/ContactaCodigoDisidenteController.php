     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'nameCodigo' => 'required',
            'emailCodigo' => 'required|email',
            'mensajeCodigo' => 'required'
        ]);
        $consulta = new ContactaCodigoDisidenteMailable($request->all());
        Mail::to('codigodisidente@gmail.com')->send($consulta);

        return back()->with('success_message', 'Gracias!Tu mensaje se ha enviado correctamente. Código Disidente te contestará lo más pronto posible.');
    }


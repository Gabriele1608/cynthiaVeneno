    {
        $request->validate([
            'name' => 'required',
            'email' => 'required|email',
            'mensaje' => 'required'
        ]);
        $consulta = new ContactaMailable($request->all());
        Mail::to('cynthiailustra@gmail.com')->send($consulta);

        return back()->with('success_message', 'Gracias!Tu mensaje se ha enviado correctamente. Te contestaré lo más pronto posible.');
        
